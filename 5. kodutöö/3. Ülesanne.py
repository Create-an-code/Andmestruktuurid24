#Binaarne otsingupuu (Binary Search Tree) koosneb sõlmedest, millel võib olla kaks last (millel omakorda võivad olla kaks last jne)
#Vasak laps on väiksem kui tema vanem, parem laps on suurem kui tema vanem
#Juur on nö mediaan või keskmine element

#Kui puu on tasakaalustamata siis otsimine läheb aeglasemaks - O(logn) -> O(n)
#Puud saab tasakaalustada kasutades pööramist (rotation) - vasak- ja parempööramine
#Lisaks saab tasakaalustamiseks kasutada punane-must puud
#Alati saab tasakaalustada ka andes sisse kindlaid andmeid ;)