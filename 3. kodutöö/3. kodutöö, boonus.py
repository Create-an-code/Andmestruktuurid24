#Fibonacci Search on algoritm, mis kasutab Fibonacci arve massiivi otsinguruumi väiksemaks jagamiseks.
#Fibonacci Search alustab suurimast Fibonacci arvust, mis on väiksem kui massiivi pikkus.
#Seejärel jagatakse massiiv Fibonacci arvuga ja kontrollitakse jagamispunkti.
#Fibonacci Search ei pea tegema jagamistehteid, mis teeb selle kiiremaks.

#Fibonacci Search on parem valik kui Ternary ja Binary Search juhul kui tegemist on suure massiiviga või kui tegemist on aeglase seadmega.
#Aeglane seade võib olla näiteks magnetkettaseade, millele salvestakse pikaajalise hoiustamise jaoks faile.